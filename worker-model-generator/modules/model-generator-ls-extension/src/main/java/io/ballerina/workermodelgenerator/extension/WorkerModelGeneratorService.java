package io.ballerina.workermodelgenerator.extension;

import io.ballerina.compiler.api.SemanticModel;
import io.ballerina.projects.Document;
import io.ballerina.workermodelgenerator.core.ModelGenerator;
import org.ballerinalang.annotation.JavaSPIService;
import org.ballerinalang.langserver.commons.service.spi.ExtendedLanguageServerService;
import org.ballerinalang.langserver.commons.workspace.WorkspaceManager;
import org.eclipse.lsp4j.jsonrpc.services.JsonRequest;
import org.eclipse.lsp4j.jsonrpc.services.JsonSegment;
import org.eclipse.lsp4j.services.LanguageServer;

import java.nio.file.Path;
import java.util.Optional;
import java.util.concurrent.CompletableFuture;

/**
 * Represents the extended language server service for the worker model generator service.
 *
 * @since 2201.9.0
 */
@JavaSPIService("org.ballerinalang.langserver.commons.service.spi.ExtendedLanguageServerService")
@JsonSegment("workerDesignService")
public class WorkerModelGeneratorService implements ExtendedLanguageServerService {

    private WorkspaceManager workspaceManager;

    @Override
    public void init(LanguageServer langServer, WorkspaceManager workspaceManager) {
        this.workspaceManager = workspaceManager;
    }

    @Override
    public Class<?> getRemoteInterface() {
        return null;
    }

    @JsonRequest
    public CompletableFuture<WorkerModelGeneratorServiceResponse> getWorkerDesignModel(
            WorkerModelGeneratorServiceRequest request) {

        return CompletableFuture.supplyAsync(() -> {
            WorkerModelGeneratorServiceResponse response = new WorkerModelGeneratorServiceResponse();
            try {
                Path filePath = Path.of(request.getFilePath());

                // Obtain the semantic model and the document
                this.workspaceManager.loadProject(filePath);
                Optional<SemanticModel> semanticModel = this.workspaceManager.semanticModel(filePath);
                Optional<Document> document = this.workspaceManager.document(filePath);
                if (semanticModel.isEmpty() || document.isEmpty()) {
                    return response;
                }

                // Generate the worker design model
                ModelGenerator modelGenerator =
                        new ModelGenerator(semanticModel.get(), document.get(), request.getLineRange());
                response.setWorkerDesignModel(modelGenerator.getWorkerModel());
            } catch (Throwable e) {
                throw new RuntimeException(e);
                //TODO: Handle errors generated by the worker model generator service.
            }
            return response;
        });
    }
}
